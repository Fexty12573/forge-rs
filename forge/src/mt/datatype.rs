#[cfg(feature = "allocator")]
impl Display for MtType {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        write!(f, "{}", alloc::format!("{:?}", self).to_lowercase())
    }
}
